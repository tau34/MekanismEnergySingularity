    private Level world;
    private MultiblockManager<AdvancedFusionMultiblockData> manager;
    private Structure structure;
    private BlockPos minPos;
    private VoxelCuboid cuboid;

    @Override
    public void init(Level world, MultiblockManager<AdvancedFusionMultiblockData> manager, Structure structure) {
        this.world = world;
        this.manager = manager;
        this.structure = structure;
    }

    @Override
    public boolean precheck() {
        Map<BlockPos, IMultiblockBase> nodes = ((StructureMixin)structure).getNodes();
        if (nodes.isEmpty()) {
            return false;
        }
        int minX = Integer.MAX_VALUE;
        int minY = Integer.MAX_VALUE;
        int minZ = Integer.MAX_VALUE;
        int maxX = Integer.MIN_VALUE;
        int maxY = Integer.MIN_VALUE;
        int maxZ = Integer.MIN_VALUE;
        for (BlockPos pos : nodes.keySet()) {
            if (pos.getX() < minX) {
                minX = pos.getX();
            }
            if (pos.getY() < minY) {
                minY = pos.getY();
            }
            if (pos.getZ() < minZ) {
                minZ = pos.getZ();
            }
            if (maxX < pos.getX()) {
                maxX = pos.getX();
            }
            if (maxY < pos.getY()) {
                maxY = pos.getY();
            }
            if (maxZ < pos.getZ()) {
                maxZ = pos.getZ();
            }
        }

        minPos = new BlockPos(minX, minY, minZ);
        cuboid = new VoxelCuboid(minPos, new BlockPos(maxX, maxY, maxZ));
        return true;
    }

    @Override
    public FormationProtocol.FormationResult validate(FormationProtocol<AdvancedFusionMultiblockData> ctx, Long2ObjectMap<ChunkAccess> chunkMap) {
        int ySize = STRUCTURE.length;
        int xSize = STRUCTURE[0].length;
        int zSize = STRUCTURE[0][0].length;

        for (int x = 0; x < xSize; x++) {
            for (int z = 0; z < zSize; z++) {
                for (int y = 0; y < ySize; y++) {
                    BlockPos offset = minPos.offset(x, y, z);
                    BlockState state = world.getBlockState(offset);
                    Block block = world.getBlockState(offset).getBlock();
                    byte type = STRUCTURE[y][x][z];
                    if (!switch (type) {
                        case 0 -> true;
                        case 1 -> state.isAir();
                        case 2 -> BlockType.is(block, MESBlockTypes.ADVANCED_FUSION_FRAME, MESBlockTypes.ADVANCED_FUSION_PORT);
                        default -> throw new IllegalStateException("Unexpected value: " + type);
                    }) {
                        return FormationProtocol.FormationResult.FAIL;
                    } else {
                        ctx.locations.add(offset);
                    }
                }
            }
        }
        return FormationProtocol.FormationResult.SUCCESS;
    }

    @Override
    public FormationProtocol.FormationResult postcheck(AdvancedFusionMultiblockData structure, Long2ObjectMap<ChunkAccess> chunkMap) {
        return FormationProtocol.FormationResult.SUCCESS;
    }

    @Override
    public IShape getShape() {
        return this.cuboid;
    }
